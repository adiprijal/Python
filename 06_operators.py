# Assignment Operators
a = 50
a += 6
a -= 56
a *= 54
a /= 30
print(a)

# Comparison Operators
# b = (14<=7)
# b = (14>=7)
# b = (14<7)
# b = (14>7)
# b = (14==7)
b = (14!=7)
# != means not equal to
