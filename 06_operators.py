# Logical Operators ( for booleans )
bool1 = True
bool2 = False
print("The value of bool1 and bool2 is", (bool1 and bool2))
      # 'and' operator prints true only if all the values are true
print("The value of bool1 or bool2 is", (bool1 or bool2))
      # 'or' operator prints true if any of the value is true 
print("The value of not bool2 is", (not bool2))
      #  'not' is for only one bool and prints the opposite value 